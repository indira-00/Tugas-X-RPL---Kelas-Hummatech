public class oper4 {
        public static void main(String[] args) {
            // Mendefinisikan nilai siswa
            int nilai1 = 80;
            int nilai2 = 90;
            int nilai3 = 75;
    
            // Menggunakan operator assignment untuk menghitung total
            int totalNilai = nilai1 + nilai2 + nilai3; // totalNilai sekarang 245
    
            // Menghitung rata-rata
            int jumlahSiswa = 3;
            double rataRataNilai = (double) totalNilai / jumlahSiswa; // rataRataNilai sekarang 81.67
    
            // Menampilkan hasil
            System.out.println("Total Nilai: " + totalNilai);
            System.out.println("Rata-rata Nilai: " + rataRataNilai);
        }
    }

